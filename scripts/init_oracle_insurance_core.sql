CREATE TABLE BRANCH (
    BRANCH_ID NUMBER PRIMARY KEY,
    BRANCH_NAME VARCHAR2(100) NOT NULL,
    CITY VARCHAR2(100),
    REGION_CODE VARCHAR2(50)
);

CREATE TABLE CLIENT_ACCOUNT (
    CLIENT_ID NUMBER PRIMARY KEY,
    CLIENT_NUMBER VARCHAR2(50) NOT NULL,
    CLIENT_TYPE VARCHAR2(20) NOT NULL,
    DISPLAY_NAME VARCHAR2(255) NOT NULL,
    SHORT_NAME VARCHAR2(255),
    LEGAL_FORM VARCHAR2(100),
    STATUS_CODE VARCHAR2(50),
    OPENED_AT DATE,
    CLOSED_AT DATE,
    BRANCH_ID NUMBER,
    RISK_CLASS VARCHAR2(20),
    INTERNAL_SEGMENT VARCHAR2(50),
    CONSTRAINT FK_CLIENT_BRANCH FOREIGN KEY (BRANCH_ID) REFERENCES BRANCH(BRANCH_ID)
);

CREATE TABLE CUSTOMER_IDENTIFIER (
    IDENTIFIER_ID NUMBER PRIMARY KEY,
    CLIENT_ID NUMBER NOT NULL,
    IDENTIFIER_TYPE VARCHAR2(30) NOT NULL,
    IDENTIFIER_VALUE VARCHAR2(100) NOT NULL,
    VALID_FROM DATE,
    VALID_TO DATE,
    CONSTRAINT FK_IDENTIFIER_CLIENT FOREIGN KEY (CLIENT_ID) REFERENCES CLIENT_ACCOUNT(CLIENT_ID)
);

CREATE TABLE CUSTOMER_ADDRESS (
    ADDRESS_ID NUMBER PRIMARY KEY,
    CLIENT_ID NUMBER NOT NULL,
    ADDRESS_TYPE VARCHAR2(50) NOT NULL,
    ADDRESS_LINE VARCHAR2(255),
    CITY VARCHAR2(100),
    POSTAL_CODE VARCHAR2(20),
    COUNTRY VARCHAR2(50),
    VALID_FROM DATE,
    VALID_TO DATE,
    CONSTRAINT FK_ADDRESS_CLIENT FOREIGN KEY (CLIENT_ID) REFERENCES CLIENT_ACCOUNT(CLIENT_ID)
);

CREATE TABLE CLIENT_CONTACT_LOG (
    CONTACT_ID NUMBER PRIMARY KEY,
    CLIENT_ID NUMBER NOT NULL,
    CONTACT_KIND VARCHAR2(30) NOT NULL,
    CONTACT_VALUE VARCHAR2(255) NOT NULL,
    IS_CONFIRMED NUMBER(1) DEFAULT 0 NOT NULL,
    CREATED_AT DATE,
    SOURCE_CHANNEL VARCHAR2(50),
    CONSTRAINT FK_CONTACT_CLIENT FOREIGN KEY (CLIENT_ID) REFERENCES CLIENT_ACCOUNT(CLIENT_ID)
);

CREATE TABLE PAYMENT_ACCOUNT (
    PAYMENT_ACCOUNT_ID NUMBER PRIMARY KEY,
    CLIENT_ID NUMBER NOT NULL,
    IBAN VARCHAR2(50) NOT NULL,
    BANK_NAME VARCHAR2(100),
    IS_ACTIVE NUMBER(1) DEFAULT 1 NOT NULL,
    VALID_FROM DATE,
    VALID_TO DATE,
    CONSTRAINT FK_PAYMENT_CLIENT FOREIGN KEY (CLIENT_ID) REFERENCES CLIENT_ACCOUNT(CLIENT_ID)
);

CREATE TABLE CONTRACT (
    CONTRACT_ID NUMBER PRIMARY KEY,
    CONTRACT_NO VARCHAR2(50) NOT NULL,
    PRODUCT_CODE VARCHAR2(50),
    START_DATE DATE,
    END_DATE DATE,
    CONTRACT_STATUS VARCHAR2(50),
    SALES_CHANNEL VARCHAR2(50)
);

CREATE TABLE CONTRACT_PARTY (
    CONTRACT_ID NUMBER NOT NULL,
    CLIENT_ID NUMBER NOT NULL,
    PARTY_ROLE VARCHAR2(50) NOT NULL,
    IS_MAIN_PARTY NUMBER(1) DEFAULT 0 NOT NULL,
    CONSTRAINT PK_CONTRACT_PARTY PRIMARY KEY (CONTRACT_ID, CLIENT_ID, PARTY_ROLE),
    CONSTRAINT FK_CONTRACT_PARTY_CONTRACT FOREIGN KEY (CONTRACT_ID) REFERENCES CONTRACT(CONTRACT_ID),
    CONSTRAINT FK_CONTRACT_PARTY_CLIENT FOREIGN KEY (CLIENT_ID) REFERENCES CLIENT_ACCOUNT(CLIENT_ID)
);

CREATE TABLE AGENT_ASSIGNMENT (
    ASSIGNMENT_ID NUMBER PRIMARY KEY,
    CLIENT_ID NUMBER NOT NULL,
    AGENT_FIRST_NAME VARCHAR2(100),
    AGENT_LAST_NAME VARCHAR2(100),
    AGENT_PESEL VARCHAR2(11),
    AGENT_LICENSE_NO VARCHAR2(50),
    ROLE_NAME VARCHAR2(100),
    VALID_FROM DATE,
    VALID_TO DATE,
    CONSTRAINT FK_AGENT_CLIENT FOREIGN KEY (CLIENT_ID) REFERENCES CLIENT_ACCOUNT(CLIENT_ID)
);

CREATE TABLE RELATED_CLIENT (
    RELATION_ID NUMBER PRIMARY KEY,
    PARENT_CLIENT_ID NUMBER NOT NULL,
    CHILD_CLIENT_ID NUMBER NOT NULL,
    RELATIONSHIP_TYPE VARCHAR2(100) NOT NULL,
    VALID_FROM DATE,
    VALID_TO DATE,
    CONSTRAINT FK_RELATED_PARENT FOREIGN KEY (PARENT_CLIENT_ID) REFERENCES CLIENT_ACCOUNT(CLIENT_ID),
    CONSTRAINT FK_RELATED_CHILD FOREIGN KEY (CHILD_CLIENT_ID) REFERENCES CLIENT_ACCOUNT(CLIENT_ID)
);

