CREATE TABLE ORG_UNIT (
    ORG_UNIT_ID NUMBER PRIMARY KEY,
    UNIT_LABEL VARCHAR2(100) NOT NULL,
    HOME_TOWN VARCHAR2(100),
    AREA_BUCKET VARCHAR2(50),
    OPS_NOTE VARCHAR2(255),
    ACTIVE_FLAG NUMBER(1) DEFAULT 1 NOT NULL
);

CREATE TABLE CLIENT_ACCOUNT (
    CUST_UID NUMBER PRIMARY KEY,
    EXT_REF_NO VARCHAR2(50) NOT NULL,
    SUBJECT_KIND VARCHAR2(20) NOT NULL,
    PARTY_LABEL VARCHAR2(255) NOT NULL,
    BRAND_LABEL VARCHAR2(255),
    FORM_CD VARCHAR2(100),
    LIFE_CYCLE_CD VARCHAR2(50),
    ACTIVATION_DT DATE,
    TERMINATION_DT DATE,
    ORG_UNIT_ID NUMBER,
    RISK_BUCKET VARCHAR2(20),
    SEGMENT_HINT VARCHAR2(50),
    LEGACY_RANK NUMBER,
    SCREENING_FLAG NUMBER(1),
    SOURCE_REVISION VARCHAR2(40),
    NOTE_TXT VARCHAR2(255),
    LAST_TOUCH_TS DATE,
    CONSTRAINT FK_CLIENT_ORG_UNIT FOREIGN KEY (ORG_UNIT_ID) REFERENCES ORG_UNIT(ORG_UNIT_ID)
);

CREATE TABLE CUSTOMER_IDENTIFIER (
    REF_ID NUMBER PRIMARY KEY,
    CUST_UID NUMBER NOT NULL,
    REF_KIND VARCHAR2(30) NOT NULL,
    REF_VALUE VARCHAR2(100),
    ISSUER_HINT VARCHAR2(100),
    VERIFIED_FLAG NUMBER(1),
    QUALITY_NOTE VARCHAR2(255),
    VALID_SINCE DATE,
    VALID_UNTIL DATE,
    INGESTED_BATCH_NO VARCHAR2(40),
    CONSTRAINT FK_IDENTIFIER_CLIENT FOREIGN KEY (CUST_UID) REFERENCES CLIENT_ACCOUNT(CUST_UID)
);

CREATE TABLE CUSTOMER_ADDRESS (
    LOC_ID NUMBER PRIMARY KEY,
    CUST_UID NUMBER NOT NULL,
    LOC_ROLE VARCHAR2(50) NOT NULL,
    LINE_1 VARCHAR2(255),
    LINE_2 VARCHAR2(255),
    CITY_HINT VARCHAR2(100),
    POST_AREA VARCHAR2(20),
    ISO_MARKET VARCHAR2(50),
    GEO_ZONE VARCHAR2(100),
    COURIER_ROUTE VARCHAR2(100),
    IS_DEFAULT NUMBER(1) DEFAULT 1,
    VALID_SINCE DATE,
    VALID_UNTIL DATE,
    CONSTRAINT FK_ADDRESS_CLIENT FOREIGN KEY (CUST_UID) REFERENCES CLIENT_ACCOUNT(CUST_UID)
);

CREATE TABLE CLIENT_CONTACT_LOG (
    CONTACT_ID NUMBER PRIMARY KEY,
    CUST_UID NUMBER NOT NULL,
    CHANNEL_CODE VARCHAR2(30) NOT NULL,
    CONTACT_PAYLOAD VARCHAR2(255),
    IS_CONFIRMED NUMBER(1) DEFAULT 0 NOT NULL,
    CREATED_AT DATE,
    SOURCE_CHANNEL VARCHAR2(50),
    OPERATOR_NOTE VARCHAR2(255),
    CONSENT_FLAG NUMBER(1),
    CONSTRAINT FK_CONTACT_CLIENT FOREIGN KEY (CUST_UID) REFERENCES CLIENT_ACCOUNT(CUST_UID)
);

CREATE TABLE PAYMENT_ACCOUNT (
    PAY_REF_ID NUMBER PRIMARY KEY,
    CUST_UID NUMBER NOT NULL,
    CLEARING_REF VARCHAR2(50),
    BANK_LABEL VARCHAR2(100),
    IS_ACTIVE NUMBER(1) DEFAULT 1 NOT NULL,
    VALID_SINCE DATE,
    VALID_UNTIL DATE,
    INTERNAL_SCORE NUMBER,
    SOURCE_NOTE VARCHAR2(255),
    CONSTRAINT FK_PAYMENT_CLIENT FOREIGN KEY (CUST_UID) REFERENCES CLIENT_ACCOUNT(CUST_UID)
);

CREATE TABLE CONTRACT (
    DEAL_ID NUMBER PRIMARY KEY,
    DEAL_REF VARCHAR2(50) NOT NULL,
    PRODUCT_FAMILY VARCHAR2(50),
    BEGIN_DT DATE,
    END_DT DATE,
    DEAL_STATE VARCHAR2(50),
    SALES_PIPE VARCHAR2(50),
    PREMIUM_BUCKET VARCHAR2(50),
    UNDERWRITER_NOTE VARCHAR2(255)
);

CREATE TABLE CONTRACT_PARTY (
    DEAL_ID NUMBER NOT NULL,
    CUST_UID NUMBER NOT NULL,
    LINK_ROLE VARCHAR2(50) NOT NULL,
    IS_MAIN_LINK NUMBER(1) DEFAULT 0 NOT NULL,
    LINK_CONFIDENCE NUMBER(5,2),
    CONSTRAINT PK_CONTRACT_PARTY PRIMARY KEY (DEAL_ID, CUST_UID, LINK_ROLE),
    CONSTRAINT FK_CONTRACT_PARTY_CONTRACT FOREIGN KEY (DEAL_ID) REFERENCES CONTRACT(DEAL_ID),
    CONSTRAINT FK_CONTRACT_PARTY_CLIENT FOREIGN KEY (CUST_UID) REFERENCES CLIENT_ACCOUNT(CUST_UID)
);

CREATE TABLE AGENT_ASSIGNMENT (
    ASSIGNMENT_ID NUMBER PRIMARY KEY,
    CUST_UID NUMBER NOT NULL,
    GIVEN_TXT VARCHAR2(100),
    FAMILY_TXT VARCHAR2(100),
    NATIONAL_REF VARCHAR2(11),
    LICENSE_TOKEN VARCHAR2(50),
    ROLE_BUCKET VARCHAR2(100),
    VALID_SINCE DATE,
    VALID_UNTIL DATE,
    HR_SOURCE VARCHAR2(50),
    QUALITY_NOTE VARCHAR2(255),
    CONSTRAINT FK_AGENT_CLIENT FOREIGN KEY (CUST_UID) REFERENCES CLIENT_ACCOUNT(CUST_UID)
);

CREATE TABLE RELATED_CLIENT (
    RELATION_ID NUMBER PRIMARY KEY,
    PARENT_UID NUMBER NOT NULL,
    CHILD_UID NUMBER NOT NULL,
    LINK_BUCKET VARCHAR2(100) NOT NULL,
    VALID_SINCE DATE,
    VALID_UNTIL DATE,
    ORIGIN_HINT VARCHAR2(100),
    COMMENT_TXT VARCHAR2(255),
    CONSTRAINT FK_RELATED_PARENT FOREIGN KEY (PARENT_UID) REFERENCES CLIENT_ACCOUNT(CUST_UID),
    CONSTRAINT FK_RELATED_CHILD FOREIGN KEY (CHILD_UID) REFERENCES CLIENT_ACCOUNT(CUST_UID)
);

